-- 1. Script Oracle 19c (Tabla + Procedimiento):
-- ? Tabla
CREATE TABLE MATRICES_PROCESADAS (
    ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MATRIZ_ORIGINAL     CLOB,
    MATRIZ_INVERSA      CLOB,
    MATRIZ_RECUPERADA   CLOB,
    FECHA_REGISTRO      TIMESTAMP DEFAULT SYSTIMESTAMP
);

COMMIT;

-- ? Procedimiento almacenado
CREATE OR REPLACE PROCEDURE INSERTAR_MATRIZ(
    P_ORIGINAL     IN CLOB,
    P_INVERSA      IN CLOB,
    P_RECUPERADA   IN CLOB
)
AS
BEGIN
    INSERT INTO MATRICES_PROCESADAS (
        MATRIZ_ORIGINAL,
        MATRIZ_INVERSA,
        MATRIZ_RECUPERADA
    )
    VALUES (
        P_ORIGINAL,
        P_INVERSA,
        P_RECUPERADA
    );

    COMMIT;
END;
